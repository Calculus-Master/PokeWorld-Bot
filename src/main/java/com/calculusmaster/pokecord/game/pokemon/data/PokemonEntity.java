    private static final Map<PokemonEntity, PokemonData> POKEMON_ENTITY_DATA = new HashMap<>();

    public static void init()
    {
        for(PokemonEntity pokemon : values()) POKEMON_ENTITY_DATA.put(pokemon, new PokemonData(pokemon));
    }

    private final String apiID;
    private final String name;
    private final int dex;
    private final PokemonRarity.Rarity rarity;

    private final EnumSet<PokemonEntityFlag> flags;

    PokemonEntity(String apiID, String name, int dex, PokemonRarity.Rarity rarity, PokemonEntityFlag... flags)
    {
        this.apiID = apiID;
        this.name = name;
        this.dex = dex;
        this.rarity = rarity;

        this.flags = EnumSet.noneOf(PokemonEntityFlag.class);
        this.flags.addAll(Arrays.asList(flags));
    }

    public PokemonData data()
    {
        return POKEMON_ENTITY_DATA.get(this);
    }

    public String getImageName(boolean shiny)
    {
        //Parameter is staying just in case any shinies are not available
        return this.dex + "_" + Global.normalize(this.toString().replaceAll("_", " ")).replaceAll(" ", "_");
    }

    public String getJSONFileName()
    {
        return this.dex + "_" + Global.normalize(this.apiID.replaceAll("-", " ")).replaceAll(" ", "_");
    }

    public int getGeneration()
    {
        if(this.dex <= 151) return 1;
        else if(this.dex <= 251) return 2;
        else if(this.dex <= 386) return 3;
        else if(this.dex <= 493) return 4;
        else if(this.dex <= 649) return 5;
        else if(this.dex <= 721) return 6;
        else if(this.dex <= 809) return 7;
        else if(this.dex <= 905) return 8;
        else return 9;
    }

    public String getAPIID()
    {
        return this.apiID;
    }

    public String getName()
    {
        return this.name;
    }

    public int getDex()
    {
        return this.dex;
    }

    public PokemonRarity.Rarity getRarity()
    {
        return this.rarity;
    }

    public boolean isNotSpawnable()
    {
        return this.flags.contains(NOT_SPAWNABLE);
    }

    public static PokemonEntity cast(String str)
    {
        PokemonEntity e = Global.getEnumFromString(PokemonEntity.values(), str);
        if(e == null) e = Arrays.stream(PokemonEntity.values()).filter(entity -> entity.getName().equalsIgnoreCase(str)).findFirst().orElse(null);
        return e;
    }

    public static PokemonEntity getRandom()
    {
        return values()[new Random().nextInt(values().length)];
    }

    enum PokemonEntityFlag
    {
        NOT_SPAWNABLE,
        ;
    }
