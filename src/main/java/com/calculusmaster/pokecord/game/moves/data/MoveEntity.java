    public static final Map<MoveEntity, MoveData> MOVE_ENTITY_DATA = new HashMap<>();

    public static void init()
    {
        for(MoveEntity moveEntity : values()) if(!moveEntity.isZMove() && !moveEntity.isMaxMove()) MOVE_ENTITY_DATA.put(moveEntity, new MoveData(moveEntity));
    }

    private final String apiID;
    private final EnumSet<MoveTag> tags;

    MoveEntity(String apiID, MoveTag... tags)
    {
        this.apiID = apiID;
        this.tags = EnumSet.noneOf(MoveTag.class); this.tags.addAll(List.of(tags));
    }

    public MoveData data()
    {
        return MOVE_ENTITY_DATA.get(this);
    }

    public String getJSONFileName()
    {
        return Global.normalize(this.apiID.replaceAll("-", " ")).replaceAll(" ", "_");
    }

    public String getAPIID()
    {
        return this.apiID;
    }

    public EnumSet<MoveTag> getTags()
    {
        return this.tags;
    }

    public boolean isZMove()
    {
        return this.tags.contains(MoveTag.ZMOVE);
    }

    public boolean isMaxMove()
    {
        return this.tags.contains(MoveTag.MAX_MOVE) || this.tags.contains(MoveTag.GMAX_MOVE);
    }

    public boolean isGMaxMove()
    {
        return this.tags.contains(GMAX_MOVE);
    }

    public static MoveEntity cast(String str)
    {
        MoveEntity e = Global.getEnumFromString(values(), str);
        return e == null ? Arrays.stream(values()).filter(entity -> entity.data().getName().equalsIgnoreCase(str)).findFirst().orElse(null) : e;
    }

    public static MoveEntity getRandom()
    {
        return values()[new Random().nextInt(values().length)];
    }
